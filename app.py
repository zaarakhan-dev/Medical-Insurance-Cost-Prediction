import streamlit as st
import pickle
import pandas as pd

# Load trained model
model = pickle.load(open("model.pkl", "rb"))

st.set_page_config(
    page_title="Medical Insurance Cost Prediction",
    page_icon="🏥",
    layout="centered"
)

st.title("🏥 Medical Insurance Cost Prediction")

st.sidebar.header("About")

st.sidebar.write("""
### Model

Random Forest Regressor

### Dataset

Medical Insurance Dataset

### Target

Insurance Charges

### Author

Zaara Khan
                 
B.Tech CSE Student
""")

st.markdown("""
Predict the estimated medical insurance charges based on a person's details using a **Random Forest Machine Learning Model**.

Fill in the information below and click **Predict Insurance Cost**.
""")

st.divider()

# User Input
age = st.slider(
    "Select Age",
    min_value=18,
    max_value=65,
    value=30
)

sex = st.selectbox(
    "Select Gender",
    ["Female", "Male"]
)

bmi = st.slider(
    "Select BMI",
    min_value=15.0,
    max_value=50.0,
    value=25.0
)

children = st.slider(
    "Number of Children",
    min_value=0,
    max_value=5,
    value=0
)

smoker = st.selectbox(
    "Are you a smoker?",
    ["No", "Yes"]
)

region = st.selectbox(
    "Select Region",
    [
        "northeast",
        "northwest",
        "southeast",
        "southwest"
    ]
)

# Show selected values
st.subheader("Selected Information")

st.write("Age: ", age)
st.write("Gender: ", sex)
st.write("BMI: ", bmi)
st.write("Children: ", children)
st.write("Smoker: ", smoker)
st.write("Region: ", region)

# Save original values
selected_gender = sex
selected_smoker = smoker
selected_region = region

# Convert inputs into numbers

sex = 1 if sex == "Male" else 0

smoker = 1 if smoker == "Yes" else 0

region_dict = {
    "northeast": 0,
    "northwest": 1,
    "southeast": 2,
    "southwest": 3
}

region = region_dict[region]

input_data = pd.DataFrame({

    "age":[age],
    "sex":[sex],
    "bmi":[bmi],
    "children":[children],
    "smoker":[smoker],
    "region":[region]

})

# Prediction Button

if st.button("Predict Insurance Cost"):

    prediction = model.predict(input_data)

    st.subheader("Prediction")

    st.metric(
        label="Estimated Insurance Cost",
        value=f"${prediction[0]:,.2f}"
    )

    if prediction[0] < 10000:
        st.info("💰 Low estimated insurance cost.")

    elif prediction[0] < 25000:
        st.warning("💵 Moderate estimated insurance cost.")

    else:
        st.error("🚨 High estimated insurance cost.")
        
    st.info(
    "This prediction is an estimate generated by a machine learning model and should not be considered an official insurance quotation."
)
    

    st.markdown("---")
    st.subheader("Prediction Summary")

    if selected_smoker == "Yes":
        st.write("🚬 Smoking significantly increases insurance charges.")

    if bmi >= 30:
        st.write("⚠️ BMI is above 30, which may increase insurance costs.")

    if age >= 50:
        st.write("👴 Higher age is associated with higher medical expenses.")

    if children >= 3:
        st.write("👨‍👩‍👧 Having more dependents may slightly affect insurance costs.")

with st.expander("ℹ️ How is this prediction made?"):
    st.write("""
    The prediction is generated using a Random Forest Regression model trained on
    the Medical Insurance Charges dataset.

    The model considers:

    • Age
    • Gender
    • BMI
    • Number of Children
    • Smoking Status
    • Region

    to estimate medical insurance charges.
    """)
st.markdown("---")

st.caption(
    "Built with using Streamlit, Scikit-learn and Python | © 2026 Zaara Khan"
)